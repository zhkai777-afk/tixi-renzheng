const workbook = Workbook.create();
const firstSheet = workbook.worksheets.add(sheets[0].name);

function cellRef(col, row) {
  let n = col;
  let s = "";
  while (n > 0) {
    const r = (n - 1) % 26;
    s = String.fromCharCode(65 + r) + s;
    n = Math.floor((n - 1) / 26);
  }
  return `${s}${row}`;
}

function sheetByIndex(index, name) {
  if (index === 0) return firstSheet;
  return workbook.worksheets.add(name);
}

